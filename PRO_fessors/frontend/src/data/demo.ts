const now = Date.now();
const iso = (days: number, hours = 0) =>
  new Date(now + (days * 24 + hours) * 3_600_000).toISOString();

function chart(base: number, bias: number, seed: number) {
  return Array.from({ length: 30 }, (_, index) => {
    const wave = Math.sin((index + seed) * 0.73) * 0.018;
    const trend = ((index - 15) / 30) * bias;
    const probability = Math.min(0.96, Math.max(0.04, base + wave + trend));
    return {
      time: new Date(now - (29 - index) * 3_600_000).toISOString(),
      probability,
      volume: 300 + ((index * 137 + seed * 311) % 1_900),
    };
  });
}

